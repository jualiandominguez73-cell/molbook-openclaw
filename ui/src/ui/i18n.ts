export function autoTranslateLabel(input: string) {
  if (!input) return undefined;
  const words = input.split(/\s+/).filter(Boolean);
  let changed = false;
  const mapped = words.map((word) => {
    const raw = word.replace(/[^\w]/g, "");
    const lower = raw.toLowerCase();
    const translated = AUTO_WORDS[lower];
    if (translated) {
      changed = true;
      return word.replace(raw, translated);
    }
    return word;
  });
  return changed ? mapped.join(" ") : undefined;
}

export function autoTranslateDescription(input: string | undefined) {
  if (!input) return undefined;
  let output = input;
  let changed = false;
  for (const [pattern, value] of AUTO_PHRASES) {
    const next = output.replace(pattern, value);
    if (next !== output) {
      output = next;
      changed = true;
    }
  }
  return changed ? output : undefined;
}
