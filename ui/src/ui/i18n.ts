export function t(locale: Locale | undefined, key: string, fallback?: string) {
  const resolved = locale === "zh" ? "zh" : "en";
  return STRINGS[resolved][key] ?? STRINGS.en[key] ?? fallback ?? key;
}

export function hasTranslation(locale: Locale | undefined, key: string) {
  const resolved = locale === "zh" ? "zh" : "en";
  return Boolean(STRINGS[resolved][key]);
}

export function tFormat(
  locale: Locale | undefined,
  key: string,
  params: Record<string, string | number>,
  fallback?: string,
) {
  const template = t(locale, key, fallback);
  return Object.entries(params).reduce(
    (current, [paramKey, value]) =>
      current.replaceAll(`{{${paramKey}}}`, String(value)),
    template,
  );
}

